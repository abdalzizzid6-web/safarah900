/**
 * Normalizes input text by trimming and standardizing keys for secure translation.
 */
function normalizeTeamName(name: string): string {
  if (!name) return "";
  return name.trim().replace(/\s+/g, " ");
}

/**
 * Translates a team name to Arabic with full fallback support.
 * @param name English or original team name.
 */
export function translateTeam(name: string | undefined | null): string {
  if (!name) return "غير محدد";
  
  const normalized = normalizeTeamName(name);

  // Exact Match Check
  if (TEAM_TRANSLATIONS_AR[normalized]) {
    return TEAM_TRANSLATIONS_AR[normalized];
  }

  // Common variations checks (e.g., lowercase / uppercase check)
  const matchedKey = Object.keys(TEAM_TRANSLATIONS_AR).find(
    (key) => key.toLowerCase() === normalized.toLowerCase()
  );
  
  if (matchedKey) {
    return TEAM_TRANSLATIONS_AR[matchedKey];
  }

  // Partial substring matching if applicable or return original
  return name;
}

/**
 * Helper to measure translation coverage percentage of array of names.
 */
export function getTranslationCoverage(names: string[]): {
  total: number;
  translated: number;
  percentage: number;
} {
  if (!names || names.length === 0) return { total: 0, translated: 0, percentage: 100 };
  
  let translatedCount = 0;
  names.forEach((name) => {
    const normalized = normalizeTeamName(name);
    if (TEAM_TRANSLATIONS_AR[normalized] || Object.keys(TEAM_TRANSLATIONS_AR).some(k => k.toLowerCase() === normalized.toLowerCase())) {
      translatedCount++;
    }
  });

  return {
    total: names.length,
    translated: translatedCount,
    percentage: Math.round((translatedCount / names.length) * 100)
  };
}
